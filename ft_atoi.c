#include "minitalk.h"

int	ft_atoi(const char *str)
{
	int	x;
	int	a;
	int	y;

	y = 1;
	a = 0;
	x = 0;
	while ((str[x] >= 9 && str[x] <= 13) || str[x] == 32)
		x++;
	if (str[x] == '-' || str[x] == '+')
	{
		if (str[x] == '-')
			y = -1;
		x++;
	}
	while (str[x] >= '0' && str[x] <= '9')
	{
		a = a * 10 + (str[x] - 48);
		x++;
	}
	return (a * y);
}
